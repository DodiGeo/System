    private PageFormat getPageFormat(PrinterJob pj) {
    PageFormat pf = pj.defaultPage();
    Paper paper = pf.getPaper();
    
    double margin = 20; 
    
    paper.setImageableArea(
        margin,
        paper.getImageableY(),
        paper.getWidth() - 2 * margin,
        paper.getImageableHeight()
    );
    
    pf.setPaper(paper);
    
    return pf;
    }
