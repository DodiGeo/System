    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String fullname = txt_fname.getText();
        String idno = txt_idno.getText();
        String date = txt_date.getText();
        String amount = txt_amount.getText();

        if (fullname.isEmpty() || idno.isEmpty() || date.isEmpty() || amount.isEmpty()) {
            JOptionPane.showMessageDialog(null, "All data is required");
        } else {
            try {
                Class.forName("org.postgresql.Driver");
                Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/UserInfo", "admin", "geonzon");

                // Inserting data into the payment table
                String insertQuery = "INSERT INTO \"UserInfos\".payment(\"Fullname\", \"Id No\", \"Date\", \"Amount\") "
                        + "VALUES (?, ?, ?, ?)";

                try (PreparedStatement pst1 = conn.prepareStatement(insertQuery)) {
                    pst1.setString(1, fullname);
                    pst1.setString(2, idno);
                    pst1.setString(3, date);
                    pst1.setString(4, amount);

                    pst1.executeUpdate();
                }

                // Updating the lead table
                String updateQuery = "UPDATE \"UserInfos\".lead SET \"Status\"=? WHERE \"Id No\"=?";
                try (PreparedStatement pst2 = conn.prepareStatement(updateQuery)) {
                    pst2.setString(1, "Paid");
                    pst2.setString(2, "idno");

                    pst2.executeUpdate();
                }

                      JOptionPane.showMessageDialog(this, "Click OK to print the Receipt", "Successful", JOptionPane.INFORMATION_MESSAGE);

                //------------------- Bill Print ------------------//
                PrinterJob pj = PrinterJob.getPrinterJob();
                pj.setPrintable(new Bill_print(), getPageFormat(pj));
                try {
                    pj.print();
                } catch (PrinterException ex) {
                    ex.printStackTrace();
                }
                //------------------- Bill Print ------------------//

            } catch (ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Error Occurred! Try again", "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
       
